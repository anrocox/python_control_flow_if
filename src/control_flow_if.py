# must define the condition to evaluate and should always end with a colon
# if expression:
#    statements

# create a integer
n = 354

# check if the value of n is greater than 200
if n > 200:
    print('the value of n is greater than 200')

# check if the value of n is equal to 200
if n == 200:
    print('the value of n is equal to 200')
print('the value of n is different from 200')
